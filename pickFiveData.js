const parseTeamRound = (pickFiveDataset) => {
  for (let i = 0; i < pickFiveDataset.length; i++) {
    for (let j = 0; j < pickFiveDataset[i].teams.length; j++) {
      let team = pickFiveDataset[i].teams[j];
      pickFiveDataset[i].teams[j].round = team.points.slice(
        team.points.indexOf("(") + 1,
        team.points.indexOf(")")
      );
      pickFiveDataset[i].teams[j].points = pickFiveDataset[i].teams[
        j
      ].points.slice(0, pickFiveDataset[i].teams[j].points.indexOf(" "));
    }
  }
  return pickFiveDataset;
};

